    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $id_persona = Persona::where('id', auth()->user()->id)->firstOrFail()->id_persona;      
        /*$exist= Prestamo::where('id_persona', $id_persona)->count();
        if($exist) return redirect('/');*/
        if (!$request->ajax()) return redirect('/');
        $prestamo = new Prestamo();
        $prestamo->cuota_mensual = $request->cuota_mensual;
        $prestamo->saldo = $request->saldo;
        $prestamo->id_entidad = $request->id_entidad;        
        $prestamo->id_persona = $id_persona;
        $prestamo->save();
    }

